export function validateGlobalAssociationBaseline(rows = GLOBAL_ASSOCIATION_BASELINE) {
  const baselineRows = Array.isArray(rows) ? rows : [];
  const invalidRows = [];
  const duplicateCountries = [];
  const duplicatePrefixes = [];
  const seenCountries = new Set();
  const seenPrefixes = new Set();

  for (const row of baselineRows) {
    const country = row?.country == null ? "" : String(row.country).trim().toLowerCase();
    const prefix = row?.prefix == null ? "" : String(row.prefix).trim().toLowerCase();
    const region = row?.region == null ? "" : String(row.region).trim().toLowerCase();
    const expectedDepth = Number(row?.expectedDepth);

    if (!country || !prefix || !region || !Number.isFinite(expectedDepth) || expectedDepth < 1) {
      invalidRows.push({ country, prefix, region, expectedDepth: row?.expectedDepth, reason: "invalid_global_association_baseline_row" });
      continue;
    }

    if (seenCountries.has(country)) duplicateCountries.push(country);
    seenCountries.add(country);

    if (seenPrefixes.has(prefix)) duplicatePrefixes.push(prefix);
    seenPrefixes.add(prefix);
  }

  return {
    ok: invalidRows.length === 0 && duplicateCountries.length === 0 && duplicatePrefixes.length === 0,
    baselineAssociationCount: baselineRows.length,
    invalidRows,
    duplicateCountries,
    duplicatePrefixes
  };
}